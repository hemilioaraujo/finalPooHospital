    private String dataParaBanco(String d){
        String data = d; 
        String[] s = data.split("/"); 
        String novaData = s[2]+"/"+s[1]+"/"+s[0];
        
        return novaData;
    }
    
    private String tracoBarra(String d){
        String barra = d.replaceAll("-", "/");
        return barra;
    }
    
    
